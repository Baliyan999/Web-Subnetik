      // –£–¥–∞–ª—è–µ–º –≤—Å–µ –Ω–µ—Ü–∏—Ñ—Ä–æ–≤—ã–µ —Å–∏–º–≤–æ–ª—ã –∫—Ä–æ–º–µ +
      let cleaned = value.replace(/[^\d+]/g, '')
      
      // –ï—Å–ª–∏ –Ω–µ—Ç + –≤ –Ω–∞—á–∞–ª–µ, –¥–æ–±–∞–≤–ª—è–µ–º
      if (!cleaned.startsWith('+')) {
        cleaned = '+' + cleaned
      }
      
      // –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º –¥–ª–∏–Ω—É
      if (cleaned.length > 16) {
        cleaned = cleaned.substring(0, 16)
      }
      
      return cleaned
    }

    // –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—á–∏—Å—Ç–∫–∏ HTML —Ç–µ–≥–æ–≤ –∏ –ø–æ—Ç–µ–Ω—Ü–∏–∞–ª—å–Ω–æ –æ–ø–∞—Å–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤
    const sanitizeInput = (input) => {
      if (typeof input !== 'string') return ''
      
      return input
        .replace(/<script\b[^<]*(?:(?!<\/script>)<[^<]*)*<\/script>/gi, '') // –£–¥–∞–ª—è–µ–º script —Ç–µ–≥–∏
        .replace(/<[^>]*>/g, '') // –£–¥–∞–ª—è–µ–º –≤—Å–µ HTML —Ç–µ–≥–∏
        .replace(/javascript:/gi, '') // –£–¥–∞–ª—è–µ–º javascript: –ø—Ä–æ—Ç–æ–∫–æ–ª—ã
        .replace(/on\w+\s*=/gi, '') // –£–¥–∞–ª—è–µ–º event handlers
        .replace(/[<>'"&]/g, '') // –£–¥–∞–ª—è–µ–º –æ–ø–∞—Å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã
        .trim()
    }

    // –í–∞–ª–∏–¥–∞—Ü–∏—è –∏–º–µ–Ω–∏
    const validateName = (name) => {
      const sanitized = sanitizeInput(name)
      
      if (!sanitized) {
        return t('contact.validation.nameRequired')
      }
      
      if (sanitized.length < 2) {
        return t('contact.validation.nameMinLength')
      }
      
      if (sanitized.length > 50) {
        return t('contact.validation.nameMaxLength')
      }
      
      // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∏–º—è —Å–æ–¥–µ—Ä–∂–∏—Ç —Ç–æ–ª—å–∫–æ –±—É–∫–≤—ã –∏ –ø—Ä–æ–±–µ–ª—ã
      if (!/^[a-zA-Z–∞-—è–ê-–Ø—ë–Å\s]+$/.test(sanitized)) {
        return t('contact.validation.nameOnlyLetters')
      }
      
      return ''
    }

    // –í–∞–ª–∏–¥–∞—Ü–∏—è —Ç–µ–ª–µ—Ñ–æ–Ω–∞
    const validatePhone = (phone) => {
      const sanitized = sanitizeInput(phone)
      
      if (!sanitized) {
        return t('contact.validation.phoneRequired')
      }
      
      // –£–¥–∞–ª—è–µ–º –≤—Å–µ –ø—Ä–æ–±–µ–ª—ã, –¥–µ—Ñ–∏—Å—ã –∏ —Å–∫–æ–±–∫–∏ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
      const cleanPhone = sanitized.replace(/[\s\-\(\)]/g, '')
      
      // –ü—Ä–æ–≤–µ—Ä—è–µ–º –º–µ–∂–¥—É–Ω–∞—Ä–æ–¥–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç E.164
      if (!/^\+[1-9]\d{1,14}$/.test(cleanPhone)) {
        return t('contact.validation.phoneFormat')
      }
      
      // –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Å—Ç—Ä–∞–Ω—É
      const country = detectCountry(cleanPhone)
      const phoneDigits = cleanPhone.substring(1) // —É–±–∏—Ä–∞–µ–º +
      
      if (country) {
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º –æ–±—â—É—é –¥–ª–∏–Ω—É –Ω–æ–º–µ—Ä–∞ (–≤–∫–ª—é—á–∞—è –∫–æ–¥ —Å—Ç—Ä–∞–Ω—ã)
        const totalLength = phoneDigits.length
        
        if (totalLength < country.minLength) {
          return t('contact.validation.phoneTooShort', { country: country.name, min: country.minLength })
        }
        
        if (totalLength > country.maxLength) {
          return t('contact.validation.phoneTooLong', { country: country.name, max: country.maxLength })
        }
      } else {
        // –ï—Å–ª–∏ —Å—Ç—Ä–∞–Ω–∞ –Ω–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∞, –∏—Å–ø–æ–ª—å–∑—É–µ–º –æ–±—â–∏–µ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è
        if (phoneDigits.length < 7) {
          return t('contact.validation.phoneMinLength')
        }
        
        if (phoneDigits.length > 15) {
          return t('contact.validation.phoneMaxLength')
        }
      }
      
      return ''
    }

    // –í–∞–ª–∏–¥–∞—Ü–∏—è –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è
    const validateComment = (comment) => {
      const sanitized = sanitizeInput(comment)
      
      if (sanitized.length > 500) {
        return t('contact.validation.commentMaxLength')
      }
      
      // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞ –ø–æ–¥–æ–∑—Ä–∏—Ç–µ–ª—å–Ω—ã–µ –ø–∞—Ç—Ç–µ—Ä–Ω—ã
      const suspiciousPatterns = [
        /<script/i,
        /javascript:/i,
        /on\w+\s*=/i,
        /eval\s*\(/i,
        /document\./i,
        /window\./i
      ]
      
      for (const pattern of suspiciousPatterns) {
        if (pattern.test(comment)) {
          return t('contact.validation.commentInvalidChars')
        }
      }
      
      return ''
    }

    // –û–±—â–∞—è –≤–∞–ª–∏–¥–∞—Ü–∏—è —Ñ–æ—Ä–º—ã
    const validateForm = () => {
      errors.value = {
        name: validateName(form.value.name),
        phone: validatePhone(form.value.phone),
        comment: validateComment(form.value.comment)
      }
      
      return !Object.values(errors.value).some(error => error !== '')
    }

    const submitForm = async () => {
      // –û—á–∏—â–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–µ –æ—à–∏–±–∫–∏
      errors.value = { name: '', phone: '', comment: '' }
      
      // –í–∞–ª–∏–¥–∏—Ä—É–µ–º —Ñ–æ—Ä–º—É
      if (!validateForm()) {
        return
      }
      
      // –°–∞–Ω–∏—Ç–∏–∑–∏—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ –ø–µ—Ä–µ–¥ –æ—Ç–ø—Ä–∞–≤–∫–æ–π
      const sanitizedData = {
        name: sanitizeInput(form.value.name),
        phone: sanitizeInput(form.value.phone),
        comment: sanitizeInput(form.value.comment),
        source: '–ì–ª–∞–≤–Ω–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞'
      }
      
      // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∑–∞–≥—Ä—É–∑–∫–∏
      isSubmitting.value = true
      
      try {
        console.log('üöÄ –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞—è–≤–∫—É:', sanitizedData)
        
        // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ –Ω–∞ backend
        const response = await fetch('https://subnetik.pro/api/send-telegram', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify(sanitizedData)
        })
        
        console.log('üì° –ü–æ–ª—É—á–µ–Ω –æ—Ç–≤–µ—Ç:', response.status, response.statusText)
        
        const result = await response.json()
        console.log('üìã –†–µ–∑—É–ª—å—Ç–∞—Ç:', result)
        
        if (result.success) {
          // –£—Å–ø–µ—à–Ω–∞—è –æ—Ç–ø—Ä–∞–≤–∫–∞
          showToastSuccess(t('notifications.formSubmitted'), t('notifications.success'))
          
          // –û—á–∏—Å—Ç–∫–∞ —Ñ–æ—Ä–º—ã
          form.value = {
            name: '',
            phone: '',
            comment: ''
          }
        } else {
          // –û—à–∏–±–∫–∞ –æ—Ç —Å–µ—Ä–≤–µ—Ä–∞
          showToastError(result.error, t('notifications.error'))
        }
      } catch (error) {
        // –û—à–∏–±–∫–∞ —Å–µ—Ç–∏ –∏–ª–∏ –¥—Ä—É–≥–∞—è –æ—à–∏–±–∫–∞
        console.error('‚ùå –û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ –∑–∞—è–≤–∫–∏:', error)
        console.error('‚ùå –¢–∏–ø –æ—à–∏–±–∫–∏:', error.name)
        console.error('‚ùå –°–æ–æ–±—â–µ–Ω–∏–µ:', error.message)
        
        let errorMessage = t('notifications.formError')
        
        if (error.name === 'TypeError' && error.message.includes('fetch')) {
          errorMessage = t('notifications.connectionError')
        } else if (error.message.includes('CORS')) {
          errorMessage = t('notifications.corsError')
        }
        
        showToastError(errorMessage, t('notifications.connectionErrorTitle'))
      } finally {
        // –£–±–∏—Ä–∞–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ –∑–∞–≥—Ä—É–∑–∫–∏
        isSubmitting.value = false
      }
    }

    // Computed —Å–≤–æ–π—Å—Ç–≤–æ –¥–ª—è –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Å—Ç—Ä–∞–Ω—ã
    const detectedCountry = computed(() => {
      return detectCountry(form.value.phone)
    })

    return {
      form,
      errors,
      isSubmitting,
      submitForm,
      formatPhoneNumber,
      detectedCountry
    }
  }
}
