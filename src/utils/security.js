// üõ°Ô∏è Security Protection System for Web-Subnetik
// –ë–ª–æ–∫–∏—Ä—É–µ—Ç —Å–∫—Ä–∏–Ω—à–æ—Ç—ã, –≤—ã–¥–µ–ª–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–∞, –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ –∏ DevTools

class SecurityProtection {
  constructor() {
    this.isDevToolsOpen = false;
    this.screenshotAttempts = 0;
    this.securityEvents = [];
    this.init();
  }

  init() {
    this.blockRightClick();
    this.blockKeyboardShortcuts();
    this.blockTextSelection();
    this.blockCopyPaste();
    this.detectDevTools();
    this.addWatermark();
    this.blockScreenshots();
    this.trackSecurityEvents();
  }

  // üö´ –ë–ª–æ–∫–∏—Ä–æ–≤–∫–∞ –ø—Ä–∞–≤–æ–≥–æ –∫–ª–∏–∫–∞
  blockRightClick() {
    document.addEventListener('contextmenu', (e) => {
      e.preventDefault();
      this.logSecurityEvent('right_click_blocked');
      return false;
    });
  }

  // ‚å®Ô∏è –ë–ª–æ–∫–∏—Ä–æ–≤–∫–∞ –∫–ª–∞–≤–∏–∞—Ç—É—Ä–Ω—ã—Ö —Å–æ–∫—Ä–∞—â–µ–Ω–∏–π
  blockKeyboardShortcuts() {
    document.addEventListener('keydown', (e) => {
      // F12, Ctrl+Shift+I, Ctrl+U, Ctrl+S
      if (e.key === 'F12' || 
          (e.ctrlKey && e.shiftKey && e.key === 'I') ||
          (e.ctrlKey && e.key === 'u') ||
          (e.ctrlKey && e.key === 's') ||
          (e.ctrlKey && e.key === 'a') ||
          (e.ctrlKey && e.key === 'c') ||
          (e.ctrlKey && e.key === 'v') ||
          (e.ctrlKey && e.key === 'x')) {
        e.preventDefault();
        this.logSecurityEvent('keyboard_shortcut_blocked', { key: e.key });
        return false;
      }
    });
  }

  // üìù –ë–ª–æ–∫–∏—Ä–æ–≤–∫–∞ –≤—ã–¥–µ–ª–µ–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ (–∫—Ä–æ–º–µ –∫–Ω–æ–ø–æ–∫)
  blockTextSelection() {
    // –î–æ–±–∞–≤–ª—è–µ–º CSS –¥–ª—è –±–ª–æ–∫–∏—Ä–æ–≤–∫–∏ –≤—ã–¥–µ–ª–µ–Ω–∏—è
    const style = document.createElement('style');
    style.textContent = `
      * {
        -webkit-user-select: none !important;
        -moz-user-select: none !important;
        -ms-user-select: none !important;
        user-select: none !important;
        -webkit-touch-callout: none !important;
        -webkit-tap-highlight-color: transparent !important;
      }
      
      /* –†–∞–∑—Ä–µ—à–∞–µ–º –≤—ã–¥–µ–ª–µ–Ω–∏–µ —Ç–æ–ª—å–∫–æ –¥–ª—è –∫–Ω–æ–ø–æ–∫ –∏ –∏–Ω–ø—É—Ç–æ–≤ */
      button, input, textarea, [role="button"], .btn, .button {
        -webkit-user-select: auto !important;
        -moz-user-select: auto !important;
        -ms-user-select: auto !important;
        user-select: auto !important;
        cursor: pointer !important;
      }
      
      /* –ë–ª–æ–∫–∏—Ä—É–µ–º –ø–µ—Ä–µ—Ç–∞—Å–∫–∏–≤–∞–Ω–∏–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π */
      img {
        -webkit-user-drag: none !important;
        -khtml-user-drag: none !important;
        -moz-user-drag: none !important;
        -o-user-drag: none !important;
        user-drag: none !important;
        pointer-events: none !important;
      }
      
      /* –†–∞–∑—Ä–µ—à–∞–µ–º –∫–ª–∏–∫–∏ –ø–æ –∫–Ω–æ–ø–∫–∞–º */
      button, .btn, .button, [role="button"], a {
        pointer-events: auto !important;
        cursor: pointer !important;
      }
    `;
    document.head.appendChild(style);
  }

  // üìã –ë–ª–æ–∫–∏—Ä–æ–≤–∫–∞ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è
  blockCopyPaste() {
    document.addEventListener('copy', (e) => {
      e.preventDefault();
      this.logSecurityEvent('copy_blocked');
      return false;
    });

    document.addEventListener('paste', (e) => {
      e.preventDefault();
      this.logSecurityEvent('paste_blocked');
      return false;
    });

    document.addEventListener('cut', (e) => {
      e.preventDefault();
      this.logSecurityEvent('cut_blocked');
      return false;
    });
  }

  // üîç –î–µ—Ç–µ–∫—Ü–∏—è DevTools
  detectDevTools() {
    let devtools = {
      open: false,
      orientation: null
    };

    const threshold = 160;

    setInterval(() => {
      if (window.outerHeight - window.innerHeight > threshold || 
          window.outerWidth - window.innerWidth > threshold) {
        if (!devtools.open) {
          devtools.open = true;
          this.handleDevToolsOpen();
        }
      } else {
        if (devtools.open) {
          devtools.open = false;
          this.handleDevToolsClose();
        }
      }
    }, 500);
  }

  handleDevToolsOpen() {
    this.isDevToolsOpen = true;
    this.logSecurityEvent('devtools_opened');
    
    // –ë–ª–æ–∫–∏—Ä—É–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç –ø—Ä–∏ –æ—Ç–∫—Ä—ã—Ç–∏–∏ DevTools
    document.body.style.filter = 'blur(10px)';
    document.body.style.pointerEvents = 'none';
    
    // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–µ
    this.showSecurityWarning('DevTools detected! Content blocked for security.');
    
    // –ó–∞–∫—Ä—ã–≤–∞–µ–º DevTools —á–µ—Ä–µ–∑ 3 —Å–µ–∫—É–Ω–¥—ã
    setTimeout(() => {
      this.closeDevTools();
    }, 3000);
  }

  handleDevToolsClose() {
    this.isDevToolsOpen = false;
    document.body.style.filter = 'none';
    document.body.style.pointerEvents = 'auto';
    this.hideSecurityWarning();
  }

  closeDevTools() {
    // –ü–æ–ø—ã—Ç–∫–∞ –∑–∞–∫—Ä—ã—Ç—å DevTools (—Ä–∞–±–æ—Ç–∞–µ—Ç –Ω–µ –≤–æ –≤—Å–µ—Ö –±—Ä–∞—É–∑–µ—Ä–∞—Ö)
    if (window.chrome && window.chrome.runtime) {
      window.chrome.runtime.sendMessage({action: 'closeDevTools'});
    }
  }

  // üì∏ –ë–ª–æ–∫–∏—Ä–æ–≤–∫–∞ —Å–∫—Ä–∏–Ω—à–æ—Ç–æ–≤
  blockScreenshots() {
    document.addEventListener('keydown', (e) => {
      if (e.key === 'PrintScreen') {
        e.preventDefault();
        this.screenshotAttempts++;
        this.logSecurityEvent('screenshot_attempt', { attempts: this.screenshotAttempts });
        
        // –ë–ª–æ–∫–∏—Ä—É–µ–º —ç–∫—Ä–∞–Ω –ø—Ä–∏ –ø–æ–ø—ã—Ç–∫–µ —Å–∫—Ä–∏–Ω—à–æ—Ç–∞
        document.body.style.filter = 'blur(15px) brightness(0.3)';
        setTimeout(() => {
          document.body.style.filter = 'none';
        }, 2000);
        
        this.showSecurityWarning('Screenshot blocked! Attempt logged.');
        return false;
      }
    });
  }

  // üè∑Ô∏è –î–æ–±–∞–≤–ª–µ–Ω–∏–µ –≤–æ–¥—è–Ω—ã—Ö –∑–Ω–∞–∫–æ–≤
  addWatermark() {
    const watermark = document.createElement('div');
    watermark.id = 'security-watermark';
    watermark.innerHTML = '¬© Web-Subnetik 2024 - Protected Content';
    watermark.style.cssText = `
      position: fixed;
      top: 0;
      left: 0;
      width: 100%;
      height: 100%;
      pointer-events: none;
      z-index: 9999;
      background: repeating-linear-gradient(
        45deg,
        transparent,
        transparent 50px,
        rgba(0,0,0,0.03) 50px,
        rgba(0,0,0,0.03) 100px
      );
      display: flex;
      align-items: center;
      justify-content: center;
      font-size: 2rem;
      color: rgba(0,0,0,0.1);
      font-weight: bold;
      text-shadow: 2px 2px 4px rgba(0,0,0,0.1);
    `;
    document.body.appendChild(watermark);
  }

  // ‚ö†Ô∏è –ü–æ–∫–∞–∑ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–µ–Ω–∏–π –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏
  showSecurityWarning(message) {
    const warning = document.createElement('div');
    warning.id = 'security-warning';
    warning.textContent = message;
    warning.style.cssText = `
      position: fixed;
      top: 50%;
      left: 50%;
      transform: translate(-50%, -50%);
      background: #ff4444;
      color: white;
      padding: 20px 40px;
      border-radius: 10px;
      font-size: 1.2rem;
      font-weight: bold;
      z-index: 10000;
      box-shadow: 0 4px 20px rgba(255,68,68,0.5);
      animation: securityPulse 0.5s ease-in-out;
    `;

    // –î–æ–±–∞–≤–ª—è–µ–º –∞–Ω–∏–º–∞—Ü–∏—é
    const style = document.createElement('style');
    style.textContent = `
      @keyframes securityPulse {
        0% { transform: translate(-50%, -50%) scale(0.8); opacity: 0; }
        50% { transform: translate(-50%, -50%) scale(1.1); opacity: 1; }
        100% { transform: translate(-50%, -50%) scale(1); opacity: 1; }
      }
    `;
    document.head.appendChild(style);

    document.body.appendChild(warning);
  }

  hideSecurityWarning() {
    const warning = document.getElementById('security-warning');
    if (warning) {
      warning.remove();
    }
  }

  // üìä –õ–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ —Å–æ–±—ã—Ç–∏–π –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏
  logSecurityEvent(type, data = {}) {
    const event = {
      type,
      timestamp: Date.now(),
      userAgent: navigator.userAgent,
      url: window.location.href,
      data
    };
    
    this.securityEvents.push(event);
    
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –Ω–∞ —Å–µ—Ä–≤–µ—Ä (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ)
    this.sendSecurityEvent(event);
    
    console.warn(`üõ°Ô∏è Security Event: ${type}`, event);
  }

  sendSecurityEvent(event) {
    // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–±—ã—Ç–∏—è –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏ –Ω–∞ —Å–µ—Ä–≤–µ—Ä
    fetch('/api/security-events', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(event)
    }).catch(err => {
      console.log('Security event logging failed:', err);
    });
  }

  // üìà –û—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏–µ —Å–æ–±—ã—Ç–∏–π –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏
  trackSecurityEvents() {
    setInterval(() => {
      if (this.securityEvents.length > 0) {
        console.log(`üõ°Ô∏è Security Events Logged: ${this.securityEvents.length}`);
      }
    }, 30000); // –ö–∞–∂–¥—ã–µ 30 —Å–µ–∫—É–Ω–¥
  }

  // üîì –ú–µ—Ç–æ–¥ –¥–ª—è –æ—Ç–∫–ª—é—á–µ–Ω–∏—è –∑–∞—â–∏—Ç—ã (—Ç–æ–ª—å–∫–æ –¥–ª—è —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–∏)
  disableProtection() {
    console.warn('üõ°Ô∏è Security protection disabled!');
    document.removeEventListener('contextmenu', this.blockRightClick);
    document.removeEventListener('keydown', this.blockKeyboardShortcuts);
    document.removeEventListener('copy', this.blockCopyPaste);
    document.removeEventListener('paste', this.blockCopyPaste);
    document.removeEventListener('cut', this.blockCopyPaste);
    
    // –£–¥–∞–ª—è–µ–º –≤–æ–¥—è–Ω–æ–π –∑–Ω–∞–∫
    const watermark = document.getElementById('security-watermark');
    if (watermark) watermark.remove();
  }
}

// üöÄ –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –∑–∞—â–∏—Ç—ã
export default SecurityProtection;

// –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –∑–∞–ø—É—Å–∫ –∑–∞—â–∏—Ç—ã
if (typeof window !== 'undefined') {
  window.SecurityProtection = SecurityProtection;
  
  // –ó–∞–ø—É—Å–∫–∞–µ–º –∑–∞—â–∏—Ç—É –ø–æ—Å–ª–µ –∑–∞–≥—Ä—É–∑–∫–∏ DOM
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
      new SecurityProtection();
    });
  } else {
    new SecurityProtection();
  }
}
